/* definition of minimum-width integer types                      */
/*                                                                */
/* _nessie_u8  => unsigned int  type, at least 8 bits,            */
/*                 equivalent to unsigned char                    */
/* _nessie_u16 => unsigned int  type, at least 16 bits            */
/* _nessie_u32 => unsigned int  type, at least 32 bits            */
/*                                                                */
/* _nessie_s8, _nessie_s16, _nessie_s32 => signed counterparts of */
/* _nessie_u8, _nessie_u16, _nessie_u32                           */
/*                                                                */
/* Always use macro's NESSIE_T8(), NESSIE_T16() or NESSIE_T32()   */
/* to obtain exact-width results,                                 */
/* i.e., to specify the size of the result of each expression.    */

typedef signed char     _nessie_s8;
typedef unsigned char   _nessie_u8;

#if defined UINT_MAX && UINT_MAX >= 4294967295UL
typedef signed short    _nessie_s16;
typedef signed int      _nessie_s32;
typedef unsigned short  _nessie_u16;
typedef unsigned int    _nessie_u32;
#define _ONE32          0xFFFFFFFFU
#else                                 /* UINT_MAX && UINT_MAX >= 4294967295UL */
typedef signed int      _nessie_s16;
typedef signed long     _nessie_s32;
typedef unsigned int    _nessie_u16;
typedef unsigned long   _nessie_u32;
#define _ONE32          0xFFFFFFFFUL
#endif                                           /* !UINT_MAX >= 4294967295UL */

#define NESSIE_DIGESTBYTES  64
#define NESSIE_DIGESTBITS   (8 * NESSIE_DIGESTBYTES)                   /* 512 */

#define NESSIE_WBLOCKBYTES  64
#define NESSIE_LENGTHBYTES  32


#define NESSIE_ONE8   0xFFU
#define NESSIE_ONE16  0xFFFFU

#define NESSIE_T8( VALUE_ )   ((VALUE_) & NESSIE_ONE8)
#define NESSIE_T16( VALUE_ )  ((VALUE_) & NESSIE_ONE16)
#define NESSIE_T32( VALUE_ )  ((VALUE_) & _ONE32)

#if defined _MSC_VER
typedef unsigned __int64    _nessie_u64;
typedef signed __int64      _nessie_s64;
#define NESSIE_LL( V_ )     ((_nessie_u64) V_ ## i64)
#define NESSIE_ONE64        NESSIE_LL(0xFFFFFFFFFFFFFFFF)
#else                                                             /* _MSC_VER */
typedef unsigned long long  _nessie_u64;
typedef signed long long    _nessie_s64;
#define NESSIE_LL( V_ )     (V_ ## ULL)
#define NESSIE_ONE64        NESSIE_LL(0xFFFFFFFFFFFFFFFF)
#endif                                                           /* !_MSC_VER */


typedef struct  _nessie_t {

    /* global number of hashed bits */
    /* (256-bit counter) */
    _nessie_u8  bit_len[NESSIE_LENGTHBYTES];                     /* _nessie_t */

    /* buffer of data to hash */
    _nessie_u8  buf[NESSIE_WBLOCKBYTES];                         /* _nessie_t */

    /* current number of bits on the file_buffer */
    int  buf_bits;                                               /* _nessie_t */

    /* current (possibly incomplete) */
    /* byte slot on the file_buffer */
    int  buf_pos;                                                /* _nessie_t */

    /* the hashing state */
    _nessie_u64  hash[NESSIE_DIGESTBYTES / 8];                   /* _nessie_t */

} _nessie_t;


