/* get the hash value from the hashing state.                                 */
/* This method uses the invariant: buf_bits < NESSIE_DIGESTBITS               */
static void  _nessie_finalize(                                             /*F*/
    struct _nessie_t* const  struct_,                                      /*-*/
    unsigned char* const     result_out_ ) {                               /*-*/
    GO                                                            (UID(F8A83A));
    int          i        = 0;
    const int    buf_bits = struct_->buf_bits;
    int          buf_pos  = struct_->buf_pos;
    _nessie_u8*  bit_len  = struct_->bit_len;
    _nessie_u8*  buf      = struct_->buf;
    _nessie_u8*  digest   = result_out_;

    /* append a '1'-bit: */
    buf[buf_pos] |= 0x80U >> (buf_bits & 7);
    /* all remaining bits on the current _nessie_u8  are set to zero. */
    buf_pos++;

    /* pad with zero bits to complete                    */
    /* (N * NESSIE_WBLOCKBITS - NESSIE_LENGTHBITS) bits: */
    if (buf_pos > NESSIE_WBLOCKBYTES - NESSIE_LENGTHBYTES) {
        if (buf_pos < NESSIE_WBLOCKBYTES) {
            MEMSET(&buf[buf_pos], 0x00,
                   NESSIE_WBLOCKBYTES - buf_pos);
        }
        _nessie_process_buffer(struct_);  /* process data block */
        buf_pos = 0;                      /* reset buffer       */
    }

    if (buf_pos < NESSIE_WBLOCKBYTES - NESSIE_LENGTHBYTES) {
        MEMSET(&buf[buf_pos], 0x00,
               (NESSIE_WBLOCKBYTES - NESSIE_LENGTHBYTES) - buf_pos);
    }

    buf_pos = NESSIE_WBLOCKBYTES - NESSIE_LENGTHBYTES;

    /* append bit length of hashed data: */
    MEMCPY(&buf[NESSIE_WBLOCKBYTES - NESSIE_LENGTHBYTES], bit_len,
           NESSIE_LENGTHBYTES);

    /* process data block: */
    _nessie_process_buffer(struct_);

    /* return the completed message digest: */
    for (i = 0; i < NESSIE_DIGESTBYTES / 8; i++) {
        digest[0] = ((_nessie_u8)((struct_->hash[i] >> 56) & 0xFF));
        digest[1] = ((_nessie_u8)((struct_->hash[i] >> 48) & 0xFF));
        digest[2] = ((_nessie_u8)((struct_->hash[i] >> 40) & 0xFF));
        digest[3] = ((_nessie_u8)((struct_->hash[i] >> 32) & 0xFF));
        digest[4] = ((_nessie_u8)((struct_->hash[i] >> 24) & 0xFF));
        digest[5] = ((_nessie_u8)((struct_->hash[i] >> 16) & 0xFF));
        digest[6] = ((_nessie_u8)((struct_->hash[i] >> 8)  & 0xFF));
        digest[7] = ((_nessie_u8)((struct_->hash[i])       & 0xFF));
        digest += 8;
    }

    struct_->buf_bits = buf_bits;
    struct_->buf_pos  = buf_pos;

    RETURN(NIL);
} /* _nessie_finalize */


/* Initialize the hashing state.                                              */
static void  _nessie_init( struct _nessie_t* const  struct_ ) {            /*F*/
    GO                                                            (UID(FF8523));
    int  i;
    MEMSET(struct_->bit_len, 0x00, 32);
    struct_->buf_bits = struct_->buf_pos = 0;
    /* it's only necessary to cleanup buf[buf_pos] */
    struct_->buf[0] = 0;
    for (i = 0; i < 8; i++) {
        struct_->hash[i] = 0L;
    }
    RETURN(NIL);
} /* _nessie_init */


